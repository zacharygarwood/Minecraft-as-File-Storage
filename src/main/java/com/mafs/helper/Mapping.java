    public static Material byteToBlock(byte b) {
        return materials[b];
    }

    public static byte blockToByte(Material material) {
        for (int i = 0; i < materials.length; i++) {
            if (materials[i].equals(material)) {
                return (byte) i;
            }
        }
        return -1;
    }
